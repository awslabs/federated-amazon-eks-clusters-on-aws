import sys


class Colors:
    CEND = '\33[0m'
    HEADER = '\033[95m'
    OKBLUE = '\033[94m'
    OKGREEN = '\033[92m'
    WARNING = '\033[93m'
    FAIL = '\033[91m'
    BOLD = "\033[1m"


def log(data):
    log_prefix = f"{Colors.BOLD}EKSFedCtl{Colors.CEND}:"
    print(f"{log_prefix} {data}", flush=True)


def error(data):
    print(f"{Colors.FAIL}EKSFedCtl Error: {data}{Colors.CEND}",
          flush=True, file=sys.stderr)
